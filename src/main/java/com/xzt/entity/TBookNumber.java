package com.xzt.entity;


public class TBookNumber {

  private long seqNum;
  private long bookId;
  private long bookesNum;
  private long lendNum;
  private long number;


  public long getSeqNum() {
    return seqNum;
  }

  public void setSeqNum(long seqNum) {
    this.seqNum = seqNum;
  }


  public long getBookId() {
    return bookId;
  }

  public void setBookId(long bookId) {
    this.bookId = bookId;
  }


  public long getBookesNum() {
    return bookesNum;
  }

  public void setBookesNum(long bookesNum) {
    this.bookesNum = bookesNum;
  }


  public long getLendNum() {
    return lendNum;
  }

  public void setLendNum(long lendNum) {
    this.lendNum = lendNum;
  }


  public long getNumber() {
    return number;
  }

  public void setNumber(long number) {
    this.number = number;
  }

}
